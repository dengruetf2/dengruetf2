## <a name="create-an-azure-storage-account"></a>Erstellen eines Azure-Speicherkontos
Ihr erstes Azure-Speicherkonto erstellen Sie am einfachsten im [Azure-Portal](https://portal.azure.com). Weitere Informationen finden Sie unter [Erstellen von Speicherkonten](https://docs.microsoft.com/azure/storage/common/storage-quickstart-create-account).

Ein Azure-Speicherkonto können Sie auch mit [Azure PowerShell](../articles/storage/common/storage-powershell-guide-full.md), der [Azure-Befehlszeilenschnittstelle](../articles/storage/common/storage-azure-cli.md) oder der [Storage Resource Provider-Clientbibliothek für .NET](https://msdn.microsoft.com/library/azure/mt131037.aspx) erstellen.


